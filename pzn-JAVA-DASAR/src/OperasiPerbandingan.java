public class OperasiPerbandingan {
    public static void main(String[] args) {
        // untuk membandingan 2 buat data atau lebih
        // menghasilkan nilai boolean, benar atau salah alias true or false
        // >, <, >=, <=, ==, !=

        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
