public class TipeDataString {
    public static void main(String[] args) {
        // isinya kumpulan character alias teks
        // di java, string pake ""
        // default value string di java adalah null

        String firstName = "Charr";
        String lastName = "Gonn";

        String fullName = firstName + " " + lastName; // concatenation string alias penggabungan 2 string atau lebih

        System.out.println(firstName);
        System.out.println(lastName);
        System.out.println(fullName);
    }
}
