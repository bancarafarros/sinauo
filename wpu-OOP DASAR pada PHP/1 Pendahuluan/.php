    <h1>procedural programming</h1>
    <p>
        > instruksi dilakukan langkah demi langkah
        > memecah program menjadi bagian-bagian (function/prosedur) yang lebih kecil
        > bagian yang lebih kecil merupakan function/method/procedure/subroutine
        > dibaca secara linear/top to bottom
    </p>

    <h1>
        kelebihan procedural programming
    </h1>
    <p>
        > to the point, karena langsung menulis instruksi
        > simplicity dan kemudahan implementasi untuk compiler dan interpreter, karena to the point
        > mudah ditelusuri, karena linear/top to bottom
        > membutuhkan lebih sedikit memory, karena to the point
    </p>

    <h1>
        OOP
    </h1>
    <p>
        > menyusun semua kode program dan strukdat sebagai objek
        > objek adalah unit dasar dari program
        > bisa menyimpan data dan perilaku di dalam objek
        > objek bisa saling berinteraksi
    </p>

    <h1>
        kelebihan OOP
    </h1>
    <p>
        > lebih mudah merepesentasi dunia nyata
        > enkapsulasi untuk mencegah bagian program tertentu dijalankan di luar lingkupnya
        > abstraksi data untuk menyembunyikan kerumitan program dengan objek
        > reusability untuk menggunakan bagian program secara berulang kali
        > memudahkan skalibilitas & ekstensibilitas
        > kemudahan pengelolaan program
        > kemudahan ketika kolaborasi
        > digunakan oleh framework
    </p>
